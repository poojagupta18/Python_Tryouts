#User defined function have def keyword to define the function.
#Argument = The value passed in the function call
#Parameter = The variable inside the function is parameter

def name(name):                  #here name is called parameter
    print('My name is '+name)
    print('Hello, '+name)

name('Alice')                   #here Alice is called argument
name('Bob')

#Output:
#My name is Alice
#Hello, Alice
#My name is Bob
#Hello, Bob
